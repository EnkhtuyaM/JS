function rectangleArea(num1, num2) {
  let answer1 = num1 * num2;
  return answer1;
}
let answer1 = rectangleArea(6, 4);
console.log("Answer1:", answer1);

function rectangleP(num1, num2) {
  let answer2 = (num1 + num2) * 2;
  return answer2;
}
let answer2 = rectangleP(6, 4);
console.log("Answer2", answer2);
